class Address {
  final String label;
  final String name;
  final String province;
  final String city;
  final String district;
  final String village;
  final String fullAddress;

  Address({
    required this.label,
    required this.name,
    required this.province,
    required this.city,
    required this.district,
    required this.village,
    required this.fullAddress,
  });
}
